// variables.cpp
#include <iostream>

int main() {
    // Declare and initialize variables
    int a = 5;
    float b = 3.14;
    char c = 'A';
    std::string d = "Hello, World!";

    // Print variables
    std::cout << "Integer: " << a << std::endl;
    std::cout << "Float: " << b << std::endl;
    std::cout << "Character: " << c << std::endl;
    std::cout << "String: " << d << std::endl;

    return 0;
}